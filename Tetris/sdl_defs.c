void set_colours(Colours* colours){
    colours->r[1] = 0;
    colours->g[1] = 255;
    colours->b[1] = 255;

    colours->r[2] = 0;
    colours->g[2] = 0;
    colours->b[2] = 255;

    colours->r[3] = 255;
    colours->g[3] = 128;
    colours->b[3] = 0;

    colours->r[4] = 255;
    colours->g[4] = 255;
    colours->b[4] = 0;

    colours->r[5] = 0;
    colours->g[5] = 255;
    colours->b[5] = 0;

    colours->r[6] = 128;
    colours->g[6] = 0;
    colours->b[6] = 255;

    colours->r[7] = 255;
    colours->g[7] = 0;
    colours->b[7] = 0;
}